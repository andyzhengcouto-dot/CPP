#include <bits/stdc++.h>
using namespace std;
 
int main() 
{
    int var = 19.7;
    
    cout << var / 10 << endl;

    float float_var = 2.5;
    cout << "Ceil: " << ceil(float_var) << endl;
    cout << "Round: " << round(float_var) << endl;
    cout << "Floor: " << floor(float_var) << endl;

    return 0;
}
